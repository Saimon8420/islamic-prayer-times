/**
 * Filter verses that match any of the given contexts.
 */
export function getVersesByContext(contexts: VerseContext[]): ContextualVerse[] {
  return CONTEXTUAL_VERSES.filter(verse =>
    verse.contexts.some(vc =>
      contexts.some(c => {
        if (vc.type !== c.type) return false;
        if (c.type === 'general' || c.type === 'friday') return true;
        return vc.value === c.value;
      })
    )
  );
}
